const normalizeText = (value) =>
  String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();

const findProfessionEntry = (profession) => {
  const normalized = normalizeText(profession);

  return (
    TOP_PROFESSIONS.find(
      (item) => normalizeText(item.pt) === normalized || normalizeText(item.en) === normalized,
    ) || null
  );
};

export const getProfessionOptions = (locale = "pt") =>
  TOP_PROFESSIONS.map((profession) => ({
    id: profession.id,
    value: locale === "en" ? profession.en : profession.pt,
    alt: locale === "en" ? profession.pt : profession.en,
    pt: profession.pt,
    en: profession.en,
  }));

export const resolveProfessionLabels = (profession) => {
  const entry = findProfessionEntry(profession);
  const fallback = typeof profession === "string" ? profession.trim().slice(0, 80) : "";

  if (!entry) {
    return {
      pt: fallback,
      en: fallback,
      id: fallback,
    };
  }

  return {
    pt: entry.pt,
    en: entry.en,
    id: entry.id,
  };
};

