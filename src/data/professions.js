const getProfileFromProfession = (profession) => {
  const normalized = normalizeText(profession);

  for (const entry of PROFILE_BY_KEYWORD) {
    if (entry.keywords.some((keyword) => normalized.includes(keyword))) {
      return entry.profile;
    }
  }

  return "generic";
};

const slugifyProfession = (profession) =>
  normalizeText(profession)
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "")
    .slice(0, 36);

const createProfessionAction = ({ id, state, en, pt, currentPeriod }) => ({
  id,
  state,
  label: en,
  labels: {
    en,
    pt,
  },
  periods: [currentPeriod, "any"],
  source: "profession",
});

export const getProfessionSuggestions = ({ state, profession, currentPeriod }) => {
  const labels = resolveProfessionLabels(profession);
  const safeProfession = typeof profession === "string" ? profession.trim().slice(0, 80) : "";

  if (!labels.pt && !labels.en) {
    return [];
  }

  const profile = getProfileFromProfession(safeProfession);
  const context = PROFILE_CONTEXT[profile] || PROFILE_CONTEXT.generic;
  const professionSlug = slugifyProfession(labels.id || profession) || "profession";

  if (state === "STOPPED") {
    return [
      createProfessionAction({
        id: `profession-${professionSlug}-stopped-001`,
        state,
        en: `As a ${labels.en}, open your top ${context.en} and work for 5 minutes.`,
        pt: `Como ${labels.pt}, abra sua principal tarefa de agora e avance por 5 minutos.`,
        currentPeriod,
      }),
      createProfessionAction({
        id: `profession-${professionSlug}-stopped-002`,
        state,
        en: `In up to 10 minutes, list 1 priority and 1 next step for your ${labels.en} work block.`,
        pt: `Em até 10 minutos, liste 1 prioridade e 1 próximo passo do seu bloco de ${labels.pt}.`,
        currentPeriod,
      }),
    ];
  }

  if (state === "NEUTRAL") {
    return [
      createProfessionAction({
        id: `profession-${professionSlug}-neutral-001`,
        state,
        en: `Pick one ${context.en} and execute it for up to 15 minutes.`,
        pt: `Escolha ${context.pt} e execute por até 15 minutos.`,
        currentPeriod,
      }),
      createProfessionAction({
        id: `profession-${professionSlug}-neutral-002`,
        state,
        en: `Review your queue quickly and close 1 ${labels.en} item in up to 15 minutes.`,
        pt: `Revise sua fila rapidamente e conclua 1 item de ${labels.pt} em até 15 minutos.`,
        currentPeriod,
      }),
    ];
  }

  return [
    createProfessionAction({
      id: `profession-${professionSlug}-active-001`,
      state,
      en: `Finish a micro-deliverable related to ${context.en} in up to 15 minutes and log progress.`,
      pt: `Finalize uma microentrega ligada a ${context.pt} em até 15 minutos e registre o progresso.`,
      currentPeriod,
    }),
    createProfessionAction({
      id: `profession-${professionSlug}-active-002`,
      state,
        en: `Run a 12-minute focus block on your next ${labels.en} priority.`,
        pt: `Faça um bloco de foco de 12 minutos na sua próxima prioridade de ${labels.pt}.`,
      currentPeriod,
    }),
  ];
};
